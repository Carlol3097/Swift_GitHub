import Foundation

print(" Valor Máximo Matriz 3x3 \n")

var matriz: [[Int]] = Array(repeating: Array(repeating: 0, count: 3), count: 3)
var numeroMaximo: Int = 0
var i: Int = 0
var j: Int = 0

for x in 0..<3 {
    for y in 0..<3 {
        print(" Ingrese el número en la posición [\(x+1)][\(y+1)]: ", terminator: "")
        if let input = readLine(), let numero = Int(input) {
            matriz[x][y] = numero
        }
    }
}

print("\n")
print(" Matriz")
for x in 0..<3 {
    for y in 0..<3 {
        print(" \(matriz[x][y]) ", terminator: "")
    }
    print()
}

numeroMaximo = matriz[0][0]
i = 0
j = 0

for x in 0..<3 {
    for y in 0..<3 {
        if matriz[x][y] > numeroMaximo {
            numeroMaximo = matriz[x][y]
            i = x
            j = y
        }
    }
}

print("\n")
print(" El valor máximo es: \(numeroMaximo)")
